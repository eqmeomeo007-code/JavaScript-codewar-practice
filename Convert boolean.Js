// Convert boolean values to strings 'Yes' or 'No'.
// https://www.codewars.com/kata/53369039d7ab3ac506000467/solutions/javascript
// 28/12/2025 

function boolToWord(bool) {
    if (bool === true) {
      return "Yes";
    } else {
      return "No";
    }
  }
  
